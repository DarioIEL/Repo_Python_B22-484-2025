//OPERATORI DI CONFRONTO
//Servono a confrontare due cose. Tutti gli operatori di confronto producono un boolean
let num01 = 10;
let num02 = 6;

console.log(num01 > num02); // true
console.log(num01 < num02); // false
console.log(num01 == num02); //false
console.log(num01 != num02); //true

//In js abbiamo anche il ===
console.log("Confronto 2 numeri, 1 Number e 1 String");

let mioNum = 5;
let tuoNum = "5";

console.log(mioNum == tuoNum); //true
console.log(mioNum === tuoNum); //false 

//L'operatore === confronta il contenuto e il tipo

